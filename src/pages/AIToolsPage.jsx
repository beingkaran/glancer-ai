/* ---------------------- small shared widgets --------------------- */
function CopyButton({ text, label = 'Copy' }) {
  const [done, setDone] = useState(false);
  const onCopy = async () => {
    if (!text) return;
    try { await navigator.clipboard.writeText(text); } catch { /* clipboard blocked */ }
    setDone(true);
    setTimeout(() => setDone(false), 1600);
  };
  return (
    <button className="tool-copy-btn" onClick={onCopy} disabled={!text}>
      {done ? <CheckIcon /> : <CopyIcon />} {done ? 'Copied!' : label}
    </button>
  );
}

const wc = (s) => (s.trim().match(/[A-Za-z0-9'’-]+/g) || []).length;

/* ============================ TOOL 1 ============================= */
function HumanizerTool() {
  const [input, setInput] = useState('');
  const [contractions, setContractions] = useState(true);
  const result = useMemo(() => humanize(input, { contractions }), [input, contractions]);
  return (
    <div className="tool-grid">
      <div className="tool-pane">
        <div className="tool-pane-head">
          <span className="tool-pane-label">Your AI text</span>
          <span className="tool-meta">{wc(input)} words</span>
        </div>
        <textarea
          className="tool-textarea"
          placeholder="Paste text generated by ChatGPT, Claude or Gemini…"
          value={input}
          onChange={(e) => setInput(e.target.value)}
        />
        <label className="tool-check">
          <input type="checkbox" checked={contractions} onChange={(e) => setContractions(e.target.checked)} />
          Add natural contractions (it's, don't, you're…)
        </label>
      </div>
      <div className="tool-pane">
        <div className="tool-pane-head">
          <span className="tool-pane-label">Humanized result</span>
          {result.changes > 0 && <span className="tool-badge-pill">{result.changes} edits</span>}
        </div>
        <div className="tool-output" aria-live="polite">
          {result.text || <span className="tool-placeholder">Your rewritten text appears here…</span>}
        </div>
        <div className="tool-actions">
          <CopyButton text={result.text} label="Copy result" />
        </div>
      </div>
    </div>
  );
}

/* ============================ TOOL 2 ============================= */
function gaugeColor(score) {
  if (score >= 70) return '#EF4444';
  if (score >= 50) return '#F59E0B';
  if (score >= 32) return '#EAB308';
  return '#10B981';
}
function DetectorTool() {
  const [input, setInput] = useState('');
  const res = useMemo(() => detectAI(input), [input]);
  return (
    <div className="tool-grid">
      <div className="tool-pane">
        <div className="tool-pane-head">
          <span className="tool-pane-label">Text to analyze</span>
          <span className="tool-meta">{res.wordCount} words</span>
        </div>
        <textarea
          className="tool-textarea"
          placeholder="Paste any text — an essay, article or message — to check how AI-like it reads (min. 25 words)…"
          value={input}
          onChange={(e) => setInput(e.target.value)}
        />
      </div>
      <div className="tool-pane">
        <div className="tool-pane-head"><span className="tool-pane-label">Result</span></div>
        {res.score === null ? (
          <div className="tool-output"><span className="tool-placeholder">Add at least 25 words to run the detector…</span></div>
        ) : (
          <div className="detector-result">
            <div className="detector-gauge" style={{ '--g': gaugeColor(res.score) }}>
              <div className="detector-gauge-num">{res.score}<span>%</span></div>
              <div className="detector-gauge-label">AI likelihood</div>
            </div>
            <div className="detector-verdict" style={{ color: gaugeColor(res.score) }}>{res.verdict}</div>
            <div className="detector-factors">
              {res.factors.map((f) => (
                <div className="detector-factor" key={f.label}>
                  <div className="detector-factor-top">
                    <span>{f.label}</span><span className="detector-factor-score">{f.score}</span>
                  </div>
                  <div className="detector-bar"><div className="detector-bar-fill" style={{ width: `${f.score}%`, background: gaugeColor(res.score) }} /></div>
                  <div className="detector-factor-detail">{f.detail}</div>
                </div>
              ))}
            </div>
            <p className="tool-note">Heuristic estimate based on writing-style signals — use it as a guide, not proof.</p>
          </div>
        )}
      </div>
    </div>
  );
}

/* ============================ TOOL 3 ============================= */
function TokenTool() {
  const [input, setInput] = useState('');
  const s = useMemo(() => tokenStats(input), [input]);
  const fmt = (n) => n < 0.01 ? `$${n.toFixed(5)}` : `$${n.toFixed(4)}`;
  return (
    <div className="tool-grid">
      <div className="tool-pane">
        <div className="tool-pane-head"><span className="tool-pane-label">Your prompt / text</span></div>
        <textarea
          className="tool-textarea"
          placeholder="Paste a prompt or document to estimate tokens and API cost…"
          value={input}
          onChange={(e) => setInput(e.target.value)}
        />
        <div className="token-stats">
          <div className="token-stat"><b>{s.tokens.toLocaleString()}</b><span>tokens*</span></div>
          <div className="token-stat"><b>{s.words.toLocaleString()}</b><span>words</span></div>
          <div className="token-stat"><b>{s.chars.toLocaleString()}</b><span>characters</span></div>
          <div className="token-stat"><b>{s.sentences.toLocaleString()}</b><span>sentences</span></div>
        </div>
      </div>
      <div className="tool-pane">
        <div className="tool-pane-head"><span className="tool-pane-label">Estimated cost per call</span></div>
        <div className="token-table">
          <div className="token-row token-row-head">
            <span>Model</span><span>Ctx</span><span>Input</span><span>In + out</span>
          </div>
          {s.costs.map((m) => (
            <div className="token-row" key={m.id}>
              <span className="token-model">{m.name}</span>
              <span className="token-ctx">{m.ctx}</span>
              <span>{fmt(m.inCost)}</span>
              <span className="token-rt">{fmt(m.roundTrip)}</span>
            </div>
          ))}
        </div>
        <p className="tool-note">*Token count is a fast local estimate (±10–15%). Prices are illustrative public list rates and change over time.</p>
      </div>
    </div>
  );
}

/* ============================ TOOL 4 ============================= */
function PromptTool() {
  const [f, setF] = useState({
    role: '', task: '', audience: '', context: '',
    format: PROMPT_FORMATS[0], tone: PROMPT_TONES[0], constraints: '',
  });
  const set = (k) => (e) => setF((p) => ({ ...p, [k]: e.target.value }));
  const applyPreset = useCallback((p) => {
    setF((prev) => ({ ...prev, role: p.role, task: p.task, audience: p.audience, format: p.format, tone: p.tone }));
  }, []);
  const prompt = useMemo(() => buildPrompt(f), [f]);
  return (
    <div className="tool-grid">
      <div className="tool-pane">
        <div className="tool-pane-head"><span className="tool-pane-label">Build your prompt</span></div>
        <div className="prompt-presets">
          {PROMPT_PRESETS.map((p) => (
            <button key={p.label} className="prompt-preset" onClick={() => applyPreset(p)}>{p.label}</button>
          ))}
        </div>
        <label className="tool-field"><span>AI should act as…</span>
          <input className="tool-input" placeholder="an expert content writer" value={f.role} onChange={set('role')} />
        </label>
        <label className="tool-field"><span>Task</span>
          <input className="tool-input" placeholder="Write a 1,200-word blog post about remote work" value={f.task} onChange={set('task')} />
        </label>
        <label className="tool-field"><span>Audience (optional)</span>
          <input className="tool-input" placeholder="people new to the subject" value={f.audience} onChange={set('audience')} />
        </label>
        <label className="tool-field"><span>Context / details (optional)</span>
          <textarea className="tool-input tool-input-area" placeholder="Key facts, links, or constraints the AI should use…" value={f.context} onChange={set('context')} />
        </label>
        <div className="tool-field-row">
          <label className="tool-field"><span>Format</span>
            <select className="tool-input" value={f.format} onChange={set('format')}>
              {PROMPT_FORMATS.map((o) => <option key={o}>{o}</option>)}
            </select>
          </label>
          <label className="tool-field"><span>Tone</span>
            <select className="tool-input" value={f.tone} onChange={set('tone')}>
              {PROMPT_TONES.map((o) => <option key={o}>{o}</option>)}
            </select>
          </label>
        </div>
      </div>
      <div className="tool-pane">
        <div className="tool-pane-head"><span className="tool-pane-label">Your ready-to-paste prompt</span></div>
        <div className="tool-output tool-output-mono" aria-live="polite">
          {f.task || f.role ? prompt : <span className="tool-placeholder">Fill in a task and your prompt appears here…</span>}
        </div>
        <div className="tool-actions">
          <CopyButton text={f.task || f.role ? prompt : ''} label="Copy prompt" />
          <a className="tool-launch" href={`https://chatgpt.com/?q=${encodeURIComponent(prompt)}`} target="_blank" rel="noopener noreferrer">Open in ChatGPT →</a>
        </div>
      </div>
    </div>
  );
}

