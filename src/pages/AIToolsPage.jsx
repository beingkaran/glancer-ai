const LENGTHS = [
  { label: 'Short', ratio: 0.18 },
  { label: 'Medium', ratio: 0.32 },
  { label: 'Long', ratio: 0.5 },
];
function SummarizerTool() {
  const [input, setInput] = useState('');
  const [li, setLi] = useState(1);
  const res = useMemo(() => summarize(input, LENGTHS[li].ratio), [input, li]);
  return (
    <div className="tool-grid">
      <div className="tool-pane">
        <div className="tool-pane-head">
          <span className="tool-pane-label">Text to summarize</span>
          <span className="tool-meta">{wc(input)} words</span>
        </div>
        <textarea
          className="tool-textarea"
          placeholder="Paste an article, essay or report to condense into its key sentences…"
          value={input}
          onChange={(e) => setInput(e.target.value)}
        />
        <div className="seg-control">
          {LENGTHS.map((l, i) => (
            <button key={l.label} className={`seg-btn${i === li ? ' active' : ''}`} onClick={() => setLi(i)}>{l.label}</button>
          ))}
        </div>
      </div>
      <div className="tool-pane">
        <div className="tool-pane-head">
          <span className="tool-pane-label">Summary</span>
          {res.summary && <span className="tool-badge-pill">{res.kept}/{res.total} sentences</span>}
        </div>
        <div className="tool-output" aria-live="polite">
          {res.summary || <span className="tool-placeholder">Your summary appears here…</span>}
        </div>
        <div className="tool-actions"><CopyButton text={res.summary} label="Copy summary" /></div>
      </div>
    </div>
  );
}

